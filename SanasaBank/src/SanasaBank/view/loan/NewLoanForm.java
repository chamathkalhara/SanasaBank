    private void instalmentCountTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_instalmentCountTextActionPerformed
        double amount = Double.parseDouble(amountText.getText());
        int count = Integer.parseInt(instalmentCountText.getText());
        double instalment = amount / count;
        instalmentLabel.setText(String.format("%.2f", instalment));
        int month = count % 12;
        int year = count / 12;
        if (year > 0 && month != 0) {
            durationLabel.setText(year + " Years and " + month + " Months");
        } else if (month == 0) {
            durationLabel.setText(year + " Years");
        } else {
            durationLabel.setText(month + " Months");
        }
        Calendar calendar = Calendar.getInstance();
        calendar.roll(Calendar.MONTH, 1);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date firstPaymentDate = calendar.getTime();
        firstPaymentDateLabel.setText(sdf.format(firstPaymentDate));

        double interest = Double.parseDouble(amountText.getText()) * Integer.parseInt(rateLabel.getText()) * 30 / (365 * 100);
        double firstPayment = (instalment + interest);

        firstPaymentLabel.setText(String.format("%.2f", firstPayment));
        guarantor1Text.requestFocus();
    }//GEN-LAST:event_instalmentCountTextActionPerformed

