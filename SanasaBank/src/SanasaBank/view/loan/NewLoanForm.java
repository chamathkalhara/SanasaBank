    private void proccessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proccessButtonActionPerformed
        try {
            Loan loan = new Loan(loanIDLabel.getText(), memberIDText.getText(), LoanTypeController.getLoanTypeID((String) loanTypeCombo.getSelectedItem()), CashInHandController.getLastCashInHandID(), Double.parseDouble(amountText.getText()), Integer.parseInt(instalmentCountText.getText()), dateLabel.getText(), Double.parseDouble(amountText.getText()), Double.parseDouble(instalmentLabel.getText()), firstPaymentDateLabel.getText(), dateLabel.getText(), 0);
            int res = LoanController.addLoan(loan);

            Guarantee guarantees[] = new Guarantee[2];
            for (int i = 0; i < 2; i++) {
                guarantees[i] = new Guarantee();
                guarantees[i].setLID(loanIDLabel.getText());
            }
            guarantees[0].setMID(guarantor1Text.getText());
            guarantees[1].setMID(guarantor2Text.getText());
            int res2 = GuaranteeController.addGuarantors(guarantees[0]);
            int res3 = GuaranteeController.addGuarantors(guarantees[1]);

            CashInHandController.updateCashInHand(-Double.parseDouble(amountText.getText()));
            if (this.property != null) {
                PropertyController.addProperty(this.property);
            }

            if (res > 0 && res2 > 0 && res3 > 0) {
                JOptionPane.showMessageDialog(null, "Success...");
                BillNumberContraller.addBillNumber(Integer.parseInt(billNumberLabel.getText()));

                setLoanId();
                setBillNumber();
                emptyAll();
                memberIDText.requestFocus();
                proccessButton.setEnabled(false);

            } else {
                JOptionPane.showMessageDialog(null, "Error...");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(NewLoanForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_proccessButtonActionPerformed

