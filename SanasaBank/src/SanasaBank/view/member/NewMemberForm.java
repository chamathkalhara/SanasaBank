    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            String gender;
            if (maleButton.isSelected()) {
                gender = "Male";
            } else {
                gender = "Female";
            }
            int contactNumber = 0;
            if (!contactNumberText.getText().equals("")) {
                contactNumber = Integer.parseInt(contactNumberText.getText().substring(0,3)+contactNumberText.getText().substring(4, 11));
            }
            Member member = new Member(memberIDLabel.getText(), nameText.getText(), addressText.getText(), nicNoText.getText() + "V", birthDayText.getText(), gender, occupationText.getText(), contactNumber);
            int res = MemberController.addMember(member);
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Member added..");
                this.memberIDText.setText(memberIDLabel.getText());
                this.memberIDText.requestFocus();
                this.dispose();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewMemberForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewMemberForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

