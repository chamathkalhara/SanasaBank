    private void ifAllFilled() {
        boolean allFilled = !memberIDLabel.getText().equals("") && !nameText.getText().equals("") && !addressText.getText().equals("") && !birthDayText.getText().equals("") && !occupationText.getText().equals("");
        if (allFilled) {
            addButton.setEnabled(true);
        } else {
            addButton.setEnabled(false);
        }
    }

