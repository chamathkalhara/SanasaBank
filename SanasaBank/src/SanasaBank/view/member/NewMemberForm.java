    private void nicNoTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nicNoTextActionPerformed
        if (!nicNoText.getText().equals("")) {
            String nic = nicNoText.getText();

            String year = nic.substring(0, 2);
            String month = nic.substring(2, 5);
            year = "19" + year;
            int iYear = Integer.parseInt(year);
            int iMonth = Integer.parseInt(month);

            String gender = "Male";
            if (iMonth > 500) {
                gender = "Female";
                iMonth -= 500;
            }
            if (iYear % 4 != 0) {
                iMonth -= 1;
            }

            Calendar calendar = Calendar.getInstance();
            calendar.set(Calendar.YEAR, iYear);
            calendar.set(Calendar.DAY_OF_YEAR, iMonth);

            Date date = calendar.getTime();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dob = sdf.format(date);
            birthDayText.setText(dob);
            if (gender.equals("Male")) {
                maleButton.doClick();
            } else {
                femaleButton.doClick();
            }
            occupationText.requestFocus();
        } else {
            birthDayText.requestFocus();
        }
    }//GEN-LAST:event_nicNoTextActionPerformed

