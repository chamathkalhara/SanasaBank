    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            AccountType accountType = new AccountType();
            accountType.setName(nameText.getText());
            accountType.setRate(Integer.parseInt(rateText.getText()));
            AccountTypeController.addNewAccountType(accountType);
            JOptionPane.showMessageDialog(null, "Account Type Added Success");
            this.dispose();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAccountType.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewAccountType.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

