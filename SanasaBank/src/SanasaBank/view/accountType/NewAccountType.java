    private void rateTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rateTextKeyReleased
        Validations.rateValidation(rateText);
        if (!nameText.getText().equals("") && !rateText.getText().equals("")) {
            addButton.setEnabled(true);
        } else {
            addButton.setEnabled(false);
        }
    }//GEN-LAST:event_rateTextKeyReleased

    private void nameTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameTextKeyReleased
        if (!nameText.getText().equals("") && !rateText.getText().equals("")) {
            addButton.setEnabled(true);
        } else {
            addButton.setEnabled(false);
        }
    }//GEN-LAST:event_nameTextKeyReleased

