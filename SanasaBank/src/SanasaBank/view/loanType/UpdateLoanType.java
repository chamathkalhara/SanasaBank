    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String newName = newNameText.getText();

            LoanType loanType = new LoanType();
            loanType.setName(newName);
            if(!newRateText.getText().equals("")){
                loanType.setRate(Integer.parseInt(newRateText.getText()));
            }
            loanType.setLTID(LoanTypeController.getLoanTypeID(currentNameText.getText()));
            int res = LoanTypeController.updateLoanType(loanType);
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Update Success");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Update Failed");
                newNameText.selectAll();
                newRateText.selectAll();
                newNameText.requestFocus();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

