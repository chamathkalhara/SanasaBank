    private void nameComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_nameComboItemStateChanged
        try {
            if (!nameCombo.getSelectedItem().equals("Select Loan type")) {
                String name = (String) nameCombo.getSelectedItem();
                currentNameText.setText(name);
                currentRateText.setText(String.valueOf(LoanTypeController.getLoanRate(name)));
            } else {
                currentNameText.setText("");
                currentRateText.setText("");
                newNameText.setText("");
                newRateText.setText("");
                updateButton.setEnabled(false);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nameComboItemStateChanged

    private void nameComboKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameComboKeyReleased
        try {
            String name = "";
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                if (!nameCombo.getSelectedItem().equals("Select Loan type")) {
                name = (String) nameCombo.getSelectedItem();
                currentNameText.setText(name);
                currentRateText.setText(String.valueOf(LoanTypeController.getLoanRate(name)));
                newNameText.requestFocus();
            } else {
                currentNameText.setText("");
                currentRateText.setText("");
                newNameText.setText("");
                newRateText.setText("");
                updateButton.setEnabled(false);
            }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateAccountType.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nameComboKeyReleased

