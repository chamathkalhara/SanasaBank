    private void newRateTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newRateTextKeyReleased
        Validations.rateValidation(newRateText);
        if (!newNameText.getText().equals("") || !newRateText.getText().equals("") && !nameCombo.getSelectedItem().equals("Select Account type")) {
            updateButton.setEnabled(true);
        } else {
            updateButton.setEnabled(false);
        }
    }//GEN-LAST:event_newRateTextKeyReleased

