    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            CashInHand cashInHand = new CashInHand(AutoGenarateID.genarate(null, "C", CashInHandController.getLastCashInHandID()), dateLabel.getText(), n5000, n2000, n1000, n500, n100, n50, n20, n10, c10, c5, c2, c1, amount, 0, amount, "open");
            int res = CashInHandController.addCashInHand(cashInHand);
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Added success ..  Press ok to load main...");
                this.dispose();
                new Main(this.isAdmin).setVisible(true);
                AccountController.updateInterest();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CashInHandForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CashInHandForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

