    private void setAmount() {
        boolean notNull = !note5000Text.getText().equals("") && !note2000Text.getText().equals("") && !note1000Text.getText().equals("") && !note500Text.getText().equals("") && !note100Text.getText().equals("") && !note50Text.getText().equals("") && !note20Text.getText().equals("") && !note100Text.getText().equals("") && !coin10Text.getText().equals("") && !coin5Text.getText().equals("") && !coin2Text.getText().equals("") && !coin1Text.getText().equals("");
        if (notNull) {
            n5000 = Integer.parseInt(note5000Text.getText());
            n2000 = Integer.parseInt(note2000Text.getText());
            n1000 = Integer.parseInt(note1000Text.getText());
            n500 = Integer.parseInt(note500Text.getText());
            n100 = Integer.parseInt(note100Text.getText());
            n50 = Integer.parseInt(note50Text.getText());
            n20 = Integer.parseInt(note20Text.getText());
            n10 = Integer.parseInt(note10Text.getText());
            c10 = Integer.parseInt(coin10Text.getText());
            c5 = Integer.parseInt(coin5Text.getText());
            c2 = Integer.parseInt(coin2Text.getText());
            c1 = Integer.parseInt(coin1Text.getText());

            amount = n5000 * 5000 + n2000 * 2000 + n1000 * 1000 + n500 * 500 + n100 * 100 + n50 * 50 + n20 * 20 + n10 * 10 + c10 * 10 + c5 * 5 + c2 * 2 + c1 * 1;
            amountText.setText(String.format("%.2f", amount));
        }
    }

