    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed

        try {
            Date date = firstDateCombo.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String firstDate = sdf.format(date);

            Date date2 = secondDateCombo.getDate();
            String secondDate = sdf.format(date2);

            Connection conn = DBConnection.getInstance().getConnection();
            HashMap<String, Object> map = new HashMap<>();
            map.put("firstDate", firstDate);
            map.put("secondDate", secondDate);

            Object[] column = {"Bill Number", "ID", "Member","Date", "Type", "Amount", "Interest"};
            DefaultTableModel dtm = new DefaultTableModel(column, 0);
            ArrayList<Tranceaction> tranceactionsList = TranceactionController.getTranceactionsByDuration(firstDate, secondDate);
            ArrayList<Instalment> instalmentsList = InstalmentController.getAllInstalmentsInDuration(firstDate, secondDate);

            for (Tranceaction tranceaction : tranceactionsList) {
                Object[] row = {tranceaction.getBillNumber(), tranceaction.getAID(), tranceaction.getTID(),tranceaction.gettDate(), tranceaction.gettType(), tranceaction.getAmmount(), "-"};
                dtm.addRow(row);

            }
            Object[] columnEmpty = {"", "", "", "", "","", ""};
            dtm.addRow(columnEmpty);
            for (Instalment instalment : instalmentsList) {
                Object[] row = {instalment.getBillNumber(), instalment.getLID(), instalment.getIID(),instalment.getiDate(), "Instalment", instalment.getiAmmount(), instalment.getiInterest()};
                dtm.addRow(row);
            }

            JasperReport compileReport = JasperCompileManager.compileReport(getClass().getResourceAsStream("../../report/TransactionsInDuration.jrxml"));
            JRTableModelDataSource dateSource = new JRTableModelDataSource(dtm);
            JasperPrint fillReport = JasperFillManager.fillReport(compileReport, map, dateSource);
            JasperViewer.viewReport(fillReport, false);

        } catch (JRException ex) {
            Logger.getLogger(CashInHandDataInDuration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CashInHandDataInDuration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CashInHandDataInDuration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_viewButtonActionPerformed

